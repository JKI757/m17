package m17

import "testing"

func TestCRC(t *testing.T) {
	type args struct {
		in []uint8
	}
	tests := []struct {
		name string
		args args
		want bool
	}{
		{"empty", args{[]uint8{}}, false},
		{"good", args{
			[]uint8{
				0x05, 0x54, 0x68, 0x69, 0x73, 0x20, 0x69, 0x73, 0x20, 0x61,
				0x6e, 0x20, 0x53, 0x4d, 0x53, 0x20, 0x54, 0x65, 0x78, 0x74,
				0x20, 0x4d, 0x65, 0x73, 0x73, 0x61, 0x67, 0x65, 0x20, 0x47,
				0x65, 0x6e, 0x65, 0x72, 0x61, 0x74, 0x65, 0x64, 0x20, 0x62,
				0x79, 0x20, 0x6d, 0x31, 0x37, 0x2d, 0x70, 0x61, 0x63, 0x6b,
				0x65, 0x74, 0x2d, 0x65, 0x6e, 0x63, 0x6f, 0x64, 0x65, 0x0,
				0xfd, 0x81}}, true},
		{"bad", args{
			[]uint8{
				0x05, 0x54, 0x68, 0x69, 0x73, 0x20, 0x69, 0x73, 0x20, 0x61,
				0x6e, 0x20, 0x53, 0x4d, 0x53, 0x20, 0x54, 0x65, 0x78, 0x74,
				0x20, 0x4d, 0x65, 0x73, 0x73, 0x61, 0x67, 0x65, 0x20, 0x47,
				0x65, 0x6e, 0x65, 0x72, 0x61, 0x74, 0x65, 0x64, 0x20, 0x62,
				0x79, 0x20, 0x6d, 0x31, 0x37, 0x2d, 0x70, 0x61, 0x63, 0x6b,
				0x65, 0x74, 0x2d, 0x65, 0x6e, 0x63, 0x6f, 0x64, 0x65, 0x0,
				0xfd, 0x8F}}, false},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := CRC(tt.args.in); got != tt.want {
				t.Errorf("CRC() = %v, want %v", got, tt.want)
			}
		})
	}
}
